package model;

/**
 *
 * @author chotr
 */
public class Admin extends Account{
    private int id;
    private String name;

    public Admin() {
    }

    public Admin(int id, String name, String user, String password) {
        super(user, password);
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
